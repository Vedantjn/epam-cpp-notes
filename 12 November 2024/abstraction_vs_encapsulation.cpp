/*
Abstraction vs Encapsulation:

Abstraction:
- Abstraction is about hiding complex implementation details and showing only the necessary features
- Focuses on what an object does rather than how it does it
- Deals with the design level
- Achieved through abstract classes and interfaces
- Example: A car's steering wheel abstracts the complex steering mechanism

Encapsulation:
- Encapsulation is about bundling data and methods that operate on that data within a single unit
- Controls access to internal data using access modifiers (public, private, protected)
- Deals with the implementation level
- Achieved through access specifiers and getter/setter methods
- Example: A class that keeps its data private and provides public methods to access it

Key Differences:
1. Purpose:
   - Abstraction: Simplifies complex systems by hiding unnecessary details
   - Encapsulation: Protects data by restricting direct access

2. Implementation:
   - Abstraction: Uses abstract classes and interfaces
   - Encapsulation: Uses access specifiers and wrapper methods

3. Level:
   - Abstraction: Works at design level
   - Encapsulation: Works at implementation level

4. Focus:
   - Abstraction: What an object does
   - Encapsulation: How an object does it

5. Relationship:
   - Abstraction is achieved through encapsulation
   - Encapsulation supports abstraction
*/
