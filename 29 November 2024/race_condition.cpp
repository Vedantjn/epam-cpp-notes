// A race condition occurs in multithreaded or concurrent programming 
// when two or more threads/processes access shared resources (e.g., 
// variables, memory, or files) simultaneously, and the final outcome 
// depends on the unpredictable timing of their execution.


// Key Characteristics of Race Conditions:

// Concurrent Access:
// Multiple threads or processes operate on shared data simultaneously.

// Uncontrolled Sequence:
// The outcome depends on the order or timing of thread execution, which is typically non-deterministic.

// Incorrect or Unexpected Results:
// The shared resource is modified inconsistently or unexpectedly, leading to bugs or crashes.


// Use synchronization mechanisms like locks, atomic variables, or thread-safe structures to prevent them.