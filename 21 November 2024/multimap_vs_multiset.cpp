// Feature               Multimap                       Multiset
// Structure             Key-value pairs                Single values
// Duplicates            Duplicate keys allowed         Duplicate values allowed
// Sorting               Sorted by key                  Sorted by value
// Access                Access based on key            Access based on value
// Use Case              Key-value relationships         Collection of values
// Example              Mapping students to their grades Counting frequencies

// Which One to Use?

// Multimap:
// Use when you need to associate a key with multiple values.
// Example: A dictionary with multiple meanings for the same word.

// Multiset:
// Use when you only need a sorted collection of values, with possible duplicates.
// Example: Tracking the frequency of numbers in a sorted manner.

// Choose multimap for key-value associations with duplicate keys.
// Choose multiset for a collection of sorted values with duplicates allowed.